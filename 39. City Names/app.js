"use strict";
function city_country(city, country) {
    return `${city}, ${country}`;
}
let result1 = city_country('Islamabad', 'Pakistan');
let result2 = city_country('Paris', 'France');
let result3 = city_country('Tokyo', 'Japan');
let result4 = city_country('Dubai', 'United Arab Emirates');
console.log(result1);
console.log(result2);
console.log(result3);
console.log(result4);
